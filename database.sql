-- Create RESERVATIONS table
CREATE TABLE RESERVATIONS (
    RESERVATION_ID NUMBER PRIMARY KEY,
    CUSTOMER_NAME VARCHAR2(100),
    CONTACT_INFO VARCHAR2(100),
    ROOM_ID NUMBER,
    CHECK_IN_DATE DATE,
    CHECK_OUT_DATE DATE,
    FOREIGN KEY (ROOM_ID) REFERENCES ROOMS(ROOM_ID)
);

