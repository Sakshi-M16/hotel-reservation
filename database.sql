-- Create ROOMS table
CREATE TABLE ROOMS (
    ROOM_ID NUMBER PRIMARY KEY,
    ROOM_TYPE VARCHAR2(50),
    PRICE NUMBER(10,2),
    STATUS VARCHAR2(20) CHECK (STATUS IN ('Available', 'Booked'))
);

